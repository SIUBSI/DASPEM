# 1.3.4 - Nama Variabel (hal. 16-17)
'''
- Variabel tidak perlu dideklarasikan mempunyai tipe data tertentu.
- Jenis data dalam variabel dapat berubah-ubah.
- Penulisan variabel harus diawali dengan huruf, dan untuk karakter selanjutnya 
bisa berupa huruf atau angka.
- Dapat berupa huruf Kapital, tetapi bersifat case-sensitive, nama Kapital dengan 
kapital adalah variabel yang berlainan.
- Penulisan variabel tidak boleh dipisah oleh <spasi>.
- Untuk variabel yang terdiri dari 2 suku kata, dapat dipisah dengan simbol underscore ( _ ) seperti nama_saya.
'''
# Contoh: 
# 123angka = "angka"
# lebih$ = 50