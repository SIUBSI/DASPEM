# 3.7.3 - Operator Penugasan (hal. 42-43)

'''
(1).
Operator: =
Penjelasan: Menugaskan nilai yang ada di kanan ke operand yang ada di sebelah kiri
Contoh:
c = a + b 
menugaskan a + b 
ke c

(2).
Operator: +=
Penjelasan: Menambahkan operand yang di kanan dengan operand yang ada di kiri dan hasilnya di tugaskan ke operand yang di kiri.
Contoh:
c += a sama dengan 
c = c + a

(3).
Operator: -=
Penjelasan: Mengurangi operand yang di kanan dengan operand yang ada di kiri dan hasilnya di tugaskan ke operand yang di kiri
Contoh:
c -= a sama dengan 
c = c - a

(4).
Operator: *=
Penjelasan: Mengalikan operand yang di kanan dengan operand yang ada di kiri dan hasilnya di tugaskan ke operand yang di kiri
Contoh:
c *= a sama dengan 
c = c * a

(5).
Operator: /=
Penjelasan: Membagi operand yang di kanan dengan operand yang ada di kiri dan hasilnya di tugaskan ke operand yang di kiri
Contoh:
c /= a sama dengan 
c = c * a

(6).
Operator: **=
Penjelasan: Memangkatkan operand yang di kanan dengan operand yang ada di kiri dan hasilnya ditugaskan ke operand yang di kiri
Contoh:
c **= a sama 
dengan c = c ** a

(7).
Operator: //=
Penjelasan: Melakukan pembagian bulat operand di kanan terhadap operand di kiri dan hasilnya disimpan di operand yang di kiri
Contoh:
c //= a sama dengan 
c = c // a

(8).
Operator: %=
Penjelasan: Melakukan operasi sisa bagi operand di kanan dengan operand di kiri dan hasilnya di simpan di operand yang di kiri
Contoh:
c %= a sama dengan 
c = c % a

'''